#logical-operators
#used to combine conditional statements

#logical operator - and - Returns True if both statements are true
print("logical operator - and - Returns True if both statements are true")
m = 10
n = 20
print(m < 5 and n < 10)
print(m < 5 and n > 10)
print(m > 5 and n > 10)
print(m > 5 and n < 10)

print("\n---------------------------------------------------\n")

#logical operator - or - Returns True if one of the statements is true
print("logical operator - or - Returns True if one of the statements is true")
m = 10
n = 20
print(m < 5 or n < 10)
print(m < 5 or n > 10)
print(m > 5 or n > 10)
print(m > 5 or n < 10)

print("\n---------------------------------------------------\n")

#logical operator - not - Reverse the result
#returns False if the result is true, returns True if the result is false
print("logical operator - not - Reverse the result")
print("returns False if the result is true, returns True if the result is false")
print(not(True))
print(not(False))
