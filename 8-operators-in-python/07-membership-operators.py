#membership-operators
#used to test if a sequence is presented in an object

#membership operator - in - Returns True if a sequence with the specified value is present in the object
print("membership operator - in - Returns True if a sequence with the specified value is present in the object")
course_list = ["AWS", "Python", "DevOps"]
print("ML" in course_list)
print("AWS" in course_list)

print("\n---------------------------------------------------\n")

#membership operator - not in - Returns True if a sequence with the specified value is not present in the object
print("membership operator - not in - Returns True if a sequence with the specified value is not present in the object")
course_list = ["AWS", "Python", "DevOps"]
print("ML" not in course_list)
print("AWS" not in course_list)
