#comparison-operators
#used to compare two values, return True or False

#comparison operator - double equal (==)
print("comparison operator - double equal (==)")
m = 10
n = 20
print(m == n)

print("\n---------------------------------------------------\n")

#comparison operator - Not equal (!=)
print("comparison operator - Not equal (!=)")
m = 10
n = 20
print(m != n)

print("\n---------------------------------------------------\n")

#comparison operator - Greater than (>)
print("comparison operator - Greater than (>)")
m = 10
n = 20
print(m > n)
print(n > m)

print("\n---------------------------------------------------\n")

#comparison operator - Less than (<)
print("comparison operator - Less than (<)")
m = 10
n = 20
print(m < n)
print(n < m)

print("\n---------------------------------------------------\n")

#comparison operator - Greater than or equal to (>=)
print("comparison operator - Greater than or equal to (>=)")
m = 10
n = 20
print(m >= n)
print(n >= m)

print("\n---------------------------------------------------\n")

#comparison operator - Less than or equal to (<=)
print("comparison operator - Less than or equal to (<=)")
m = 10
n = 20
print(m <= n)
print(n <= m)

print("\n---------------------------------------------------\n")


print(12 == 12)    # Equal operator, returns True
print(12 == 15)    # Equal operator, returns False
print(12 != 12)    # Not Equal operator, returns False
print(12 != 15)    # Not Equal operator, returns True
print(15 > 12)     # Greater Than, returns True
print(15 > 15)     # Greater Than, returns False
print(12 > 15)     # Greater Than, returns False
print(12 < 15)     # Less Than, returns True
print(15 < 15)     # Less Than, returns False
print(15 < 12)     # Less Than, returns False
print(15 >= 12)    # Greater Than Equals To, returns True
print(15 >= 15)    # Greater Than Equals To, returns True
print(12 >= 15)    # Greater Than Equals To, returns False
print(12 <= 15)    # Less Than Equals To, returns True
print(15 <= 15)    # Less Than Equals To, returns True
print(15 <= 12)    # Less Than Equals To, returns False

