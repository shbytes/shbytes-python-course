#assignment-operators
#used to assign values to variables

#assignment operator - equal (=)
print("assignment operator - equal (=)")
m = 10
print(m)

print("\n---------------------------------------------------\n")

#assignment operator - plus equal (+=)
print("assignment operator - plus equal (+=)")
m = 10
m += 20
print(m)

print("\n---------------------------------------------------\n")

#assignment operator - minus equal (-=)
print("assignment operator - minus equal (-=)")
m = 10
m -= 20
print(m)

print("\n---------------------------------------------------\n")

#assignment operator - multiply equal (*=)
print("assignment operator - multiply equal (*=)")
m = 10
m *= 20
print(m)

print("\n---------------------------------------------------\n")

#assignment operator - division equal (/=)
print("assignment operator - division equal (/=)")
m = 40
m /= 10
print(m)

print("\n---------------------------------------------------\n")

#assignment operator - modulus equal (%=)
print("assignment operator - modulus equal (%=)")
m = 37
m %= 5
print(m)

print("\n---------------------------------------------------\n")

#assignment operator - floor division equal (//=)
print("assignment operator - floor division equal (//=)")
m = 37
m //= 5
print(m)

print("\n---------------------------------------------------\n")

#assignment operator - exponentiation equal (**=)
print("assignment operator - exponentiation equal (**=)")
m = 2
m **= 5
print(m)

print("\n---------------------------------------------------\n")

#assignment operator - Bitwise OR equal (|=)
print("assignment operator - Bitwise OR equal (|=)")
m = 2
m |= 5
print(m)

print("\n---------------------------------------------------\n")

#assignment operator - Bitwise XOR equal (^=)
print("assignment operator - Bitwise XOR equal (^=)")
m = 2
m ^= 5
print(m)

print("\n---------------------------------------------------\n")

#assignment operator - Bitwise Zero fill left shift equal (>>=)
print("assignment operator - Bitwise Zero fill left shift equal (>>=)")
m = 128
m >>= 5
print(m)

print("\n---------------------------------------------------\n")

#assignment operator - Bitwise Signed right shift equal (<<=)
print("assignment operator - Bitwise Signed right shift equal (<<=)")
m = 2
m <<= 5
print(m)

print("\n---------------------------------------------------\n")

